#ifndef __base64_h__
#define __base64_h__

/////////////////////////////////////////////////////////////////////////////
// codec
/////////////////////////////////////////////////////////////////////////////
// 解码：__in中的字符数一定是4的整倍数
// 返回解码长度
int	base64_decode(char *__in, int __inlen, unsigned char *__out);

// 编码：__out的长度一定是4的整倍数, 会自动0结尾
// 返回编码长度
int	base64_encode(unsigned char *__in, int __inlen, char *__out);

#endif	// EOF __base64_h__
